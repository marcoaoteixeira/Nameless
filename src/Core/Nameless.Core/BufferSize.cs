namespace Nameless;

public enum BufferSize {
    /// <summary>
    /// 1Kb
    /// </summary>
    Micro = 1024,

    /// <summary>
    /// 2Kb
    /// </summary>
    Tiny = 1024 * 2,

    /// <summary>
    /// 4Kb
    /// </summary>
    Small = 1024 * 4,

    /// <summary>
    /// 8Kb
    /// </summary>
    Moderate = 1024 * 8,

    /// <summary>
    /// 16Kb
    /// </summary>
    Medium = 1024 * 16,

    /// <summary>
    /// 32Kb
    /// </summary>
    Considerable = 1024 * 32,

    /// <summary>
    /// 64Kb
    /// </summary>
    Large = 1024 * 64,

    /// <summary>
    /// 128Kb
    /// </summary>
    Big = 1024 * 128,

    /// <summary>
    /// 1Mb
    /// </summary>
    Huge = 1024 * 1024,

    /// <summary>
    /// 2Mb
    /// </summary>
    Massive = 1024 * 1024 * 2,

    /// <summary>
    /// 4Mb
    /// </summary>
    Enormous = 1024 * 1024 * 4,

    /// <summary>
    /// 8Mb
    /// </summary>
    Humongous = 1024 * 1024 * 8,
}