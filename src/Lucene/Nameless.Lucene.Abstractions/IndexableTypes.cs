namespace Nameless.Lucene {

    /// <summary>
    /// Enumerator for indexable types.
    /// </summary>
    public enum IndexableType {
        /// <summary>
        /// Integers
        /// </summary>
        Integer,
        /// <summary>
        /// String
        /// </summary>
        Text,
        /// <summary>
        /// Date/Time
        /// </summary>
        DateTime,
        /// <summary>
        /// Boolean
        /// </summary>
        Boolean,
        /// <summary>
        /// Float point or decimal numbers.
        /// </summary>
        Number
    }
}
